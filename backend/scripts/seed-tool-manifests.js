import "dotenv/config";
import { uploadFile } from "../src/services/team/teamStorage.js";

const BUCKET = "team-prompts";

const NOTEBOOKLM_PATH = "tools/notebooklm.md";
const NOTEBOOKLM_BODY = `# NotebookLM — инструмент глубокого исследования

## Что это
NotebookLM (Google) — система, которая отвечает на вопросы по подгруженным
тобой источникам: книги, статьи, PDF, текстовые расшифровки. Каждый ответ
сопровождается цитатами из источников.

## Возможности
- Структурированные ответы по конкретным вопросам к загруженной базе.
- Цитаты с привязкой к страницам/абзацам исходника.
- Возможность задавать уточняющие вопросы — модель помнит контекст блокнота.

## Ограничения
- Отвечает ТОЛЬКО по подгруженным источникам. Если факта в них нет —
  скажет «не могу найти». Не выдумывает.
- Не ходит в интернет. Свежее знание подгружай отдельно.
- Чувствителен к качеству источников: OCR с опечатками → плохие ответы.

## Как пользоваться правильно
1. Перед началом задачи убедись, что NotebookLM-блокнот по теме создан и
   в нём подгружены релевантные источники. Если блокнота нет — попроси
   Влада создать его (агент НЕ создаёт блокноты сам).
2. Формулируй вопрос узко: «когда впервые упоминается X в источнике Y» —
   а не «расскажи всё про X».
3. Используй цитаты в финальном артефакте. Без них исследование
   фактчекать невозможно.
4. Если NotebookLM отвечает «не нашёл» — это не баг. Это сигнал, что
   источников недостаточно. Сообщи Владу.

## Самопроверка после использования
- Все ли утверждения в моём ответе подкреплены цитатами из NotebookLM?
- Не приписал ли я фактуру, которой не было в источниках?
- Указал ли я страницу/раздел, откуда взята каждая цитата?
- Сообщил ли я Владу, если каких-то фактов в источниках не нашлось?
`;

async function seedFile(path, body) {
  await uploadFile(BUCKET, path, body);
  console.log(`[seed-tools] ✓ ${BUCKET}/${path} (${body.length} chars)`);
}

async function main() {
  console.log(`[seed-tools] Загружаю стартовые методички в bucket ${BUCKET}…`);
  await seedFile(NOTEBOOKLM_PATH, NOTEBOOKLM_BODY);
  console.log("[seed-tools] Готово.");
}

main().catch((err) => {
  console.error("[seed-tools] упало:", err);
  process.exit(1);
});
