// Версия ростера команды (Сессия 10 этапа 2, пункт 12).
//
// Содержит in-memory счётчик, который инкрементируется на каждое изменение
// состава агентов (create / archive / restore / pause). promptBuilder
// сравнивает текущую версию с версией закешированного Awareness-блока для
// конкретного агента и пересобирает блок, если состав изменился.
//
// Вынесено в отдельный модуль, чтобы избежать циклической зависимости
// между agentService.js (мутации состава) и promptBuilder.js (чтение
// awareness при сборке промпта). Оба импортируют только этот лёгкий файл.
//
// Версия сбрасывается при рестарте процесса — это допустимо: при холодном
// старте кеш Awareness в promptBuilder тоже пустой, версии совпадают (обе 0),
// и первый прогон промпта собирает свежий блок.

let awarenessVersion = 0;

export function getAwarenessVersion() {
  return awarenessVersion;
}

export function bumpAwarenessVersion() {
  awarenessVersion += 1;
}
